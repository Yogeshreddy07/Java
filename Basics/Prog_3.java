public class Prog_3 {
    public static void main(String[] args){
        int rellono=26; //integer 
        char Grade='O'; // char where single character is stored
        String name="Yogesh";// String where multiple characters are stored
        double cgpa=9.8;// double where decimal values are stored
        long P = 1234567890L; // long where large integer values are stored
        boolean isActive = true; // boolean where true or false values are stored
        System.out.println("Hello, my name is "+name+".");
        System.out.println("My roll number is "+rellono+".");   
        System.out.println("My CGPA is "+cgpa+".");
        System.out.println("My grade is "+Grade+".");   
        System.out.println("My phone number is "+P+".");
        System.out.println("Am I active? "+isActive+".");
        System.out.println("This is a simple Java program that demonstrates the use of different data types.");


    }
}
